-- --------------------------------------------------------------------------------
-- 7. Automatic Profile Creation Trigger
-- --------------------------------------------------------------------------------

-- This function automatically creates a profile entry when a new user signs up
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, full_name, avatar_url, is_admin)
  VALUES (
    new.id, 
    new.raw_user_meta_data->>'full_name', 
    new.raw_user_meta_data->>'avatar_url',
    COALESCE((new.raw_user_meta_data->>'is_admin')::boolean, false)
  );
  
  -- If they are an admin, also add them to the admins table
  IF (new.raw_user_meta_data->>'is_admin')::boolean = true THEN
    INSERT INTO public.admins (id, role_title, department)
    VALUES (new.id, 'Support Specialist', COALESCE(new.raw_user_meta_data->>'department', 'General'));
  END IF;

  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger the function every time a user is created in auth.users
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();
